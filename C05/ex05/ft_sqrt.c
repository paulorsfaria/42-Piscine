#include <stdio.h>
int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while((i * i) != nb && i <(nb / 2))
	{
		i++;
	}
	return(i);
}
int	main()
{
	printf("%d", ft_sqrt(256));
}
