char *ft_strstr(char *str, char *to_find)
{
    int i;
    int c;
    
    i = 0;
    c = 0;
    if (to_find != '\0')
        return('\0');
    while (str[i] != '\0')
    {
        while (str[i] != to_find[c])
        {
            c++;
        }
        i++;
    }
}