#include <unistd.h>
void	putchar(char c)
{
	write(1, &c, 1);
}
void	ft_putnbr(int	nb)
{
	if(nb == -2147483648)
	{
		write(1, "-2", 2);
		nb = 147483648;
	}
	if(nb < 0)
	{
		write(1, "-", 1);
		nb =-nb;
	}
	if(nb >= 10)
	{
		ft_putnbr(nb/10);
		nb = nb % 10;
	}
	if(nb < 10)
		putchar(nb + 48);
}
